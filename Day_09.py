# ============ Type casting in python 100day challenger==========.
# Type casting is the process of converting a variable from one data type to another.
# There are two types of type casting in python: implicit and explicit.
# Implicit type casting is done automatically by python when we perform an operation that requires a different data type
a = 1.9
b = 8
print(a+b)
# Explicit type casting is done manually by the programmer using built-in functions like int(), float(), str
d = "7"
e = 3
print(int(d)+e)